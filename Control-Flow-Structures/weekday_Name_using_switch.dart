void main() {
  int dayNumber = 3;

  switch (dayNumber) {
    case 1:
      print("Weekday: Monday");
      break;
    case 2:
      print("Weekday: Tuesday");
      break;
    case 3:
      print("Weekday: Wednesday");
      break;
    case 4:
      print("Weekday: Thursday");
      break;
    case 5:
      print("Weekday: Friday");
      break;
    case 6:
      print("Weekday: Saturday");
      break;
    case 7:
      print("Weekday: Sunday");
      break;
    default:
      print("Invalid day");
      break;
  }
}
